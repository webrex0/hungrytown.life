function initializeApp(appId) {
    const topicSelect = document.getElementById(`topicSelect${appId}`);
    const numColumnsInput = document.getElementById(`numColumns${appId}`);
    const questionArea = document.getElementById(`questionArea${appId}`);

    // Enable number of questions dropdown only after a topic is selected
    topicSelect.addEventListener('change', function() {
        // Clear any existing questions and answers
        questionArea.innerHTML = '';
        numColumnsInput.value = ''; // Reset number of questions selection
        numColumnsInput.disabled = false;
    });

    // Auto-submit when the number of questions is selected
    numColumnsInput.addEventListener('change', function() {
        questionArea.innerHTML = ''; // Clear previous questions and answers
        const selectedTopic = topicSelect.value;
        const numColumns = parseInt(numColumnsInput.value);

        if (!selectedTopic || isNaN(numColumns) || numColumns < 1 || numColumns > 6) {
            alert('Please select a valid number of questions from 1 to 6.');
            return;
        }

        const selectedQuestions = topicQuestions[selectedTopic];
        const questionBlocks = [];

        for (let i = 0; i < numColumns; i++) {
            const questionBlock = document.createElement('div');
            questionBlock.className = 'col-md-12 question-block flexion';

            const questionSelect = document.createElement('select');
            questionSelect.className = 'form-select mb-2 w-100px';
            questionSelect.innerHTML = '<option selected disabled>选择..</option>';

            selectedQuestions.forEach(question => {
                const option = document.createElement('option');
                option.value = question;
                option.textContent = question;
                questionSelect.appendChild(option);
            });

            const answerDiv = document.createElement('div');
            answerDiv.className = 'answer';

            // Display answer based on the topic and question
            questionSelect.addEventListener('change', function() {
                const selectedQuestion = questionSelect.value;
                const answer = answers[selectedTopic][selectedQuestion] || 'Answer not available.';
                
                // Clear any existing answers and display the selected answer
                answerDiv.innerHTML = `<p>${answer}</p>`;
            });

            questionBlock.appendChild(questionSelect);
            questionBlock.appendChild(answerDiv);
            questionBlocks.push(questionBlock);
        }

        questionBlocks.forEach(block => questionArea.appendChild(block));
        updateAvailableQuestions();
    });
}

function updateAvailableQuestions() {
// Collect selected questions from all 12 sets
const selectedQuestions = [];
for (let i = 1; i <= 12; i++) {
    const selectedQuestionsInSet = Array.from(document.querySelectorAll(`#questionArea${i} select`))
        .map(select => select.value)
        .filter(value => value); // Filter out empty values
    selectedQuestions.push(...selectedQuestionsInSet);
}

// Disable selected questions across all question areas
for (let i = 1; i <= 12; i++) {
    const questionSelects = document.querySelectorAll(`#questionArea${i} select`);
    questionSelects.forEach(select => {
        Array.from(select.options).forEach(option => {
            if (selectedQuestions.includes(option.value)) {
                option.disabled = true;
                option.style.color = '#dedede'; // Grey out disabled options
            } else {
                option.disabled = false;
                option.style.color = '#2f663b'; // Dark green text for available options
            }
        });
    });
}
}


